public class LC {
    public static void main(String[] args) {
        // To loop through numbers from 1 to 100
        for (int i = 1; i <= 100; i++) {
            // This prints the current number
            System.out.print(i + " ");

            // This means if 'i' is divisible by 10, move to a new line
            if (i % 10 == 0) {
                System.out.println(); // This will start a new line
            }
        }
    }
}
